bool LexigraphicLess(const Vector3f& a, const Vector3f& b) {
  if (a.x < b.x) {
    return true;
  } else if (a.x > b.x) {
    return false;
  }

  if (a.y < b.y) {
    return true;
  }
  else if (a.y > b.y) {
    return false;
  }

  if (a.z < b.z) {
    return true;
  }
  else if (a.z > b.z) {
    return false;
  }

  // Equals, meaning not less than.
  return false;
}

Vector3f ConsistentVertexInterp(float iso_level,
  const Vector3f& x, const Vector3f& y, float leftVal, float rightVal,
  float epsilon = 1e-5f);

Vector3f VertexInterp(float iso_level,
  const Vector3f& left, const Vector3f& right,
  float leftVal, float rightVal, float epsilon = 1e-5f);

// Consistent linear interpolation.
Vector3f ConsistentVertexInterp(float iso_level,
  const Vector3f& x, const Vector3f& y, float leftVal, float rightVal,
  float epsilon) {
  Vector3f left;
  Vector3f right;
  if (LexigraphicLess(x, y)) {
    left = x;
    right = y;
  } else {
    left = y;
    right = x;
    std::swap(leftVal, rightVal);
  }

  // TODO(jiawen): move iso_level to top
  // TODO(jiawen): call it t
  if (std::abs(leftVal - rightVal) > epsilon) {
    return left +
      (right - left) / (rightVal - leftVal) * (iso_level - leftVal);
  } else {
    return left;
  }
}

// Linearly interpolate the position where an isosurface cuts
// an edge between two vertices, each with their own scalar value
Vector3f VertexInterp(float iso_level,
  const Vector3f& left, const Vector3f& right,
  float leftVal, float rightVal, float epsilon) {
  if (std::abs(iso_level - leftVal) < epsilon) {
    return left;
  }
  if (std::abs(iso_level - rightVal) < epsilon) {
    return right;
  }
  if (std::abs(leftVal - rightVal) < epsilon) {
    return left;
  }
  float t = (iso_level - leftVal) / (rightVal - leftVal);
  return left + t * (right - left);
}

void PolygonalizeCell(Vector3f grid_positions[8], Vector3f grid_normals[8],
  float grid_vals[8], float iso_level,
  vector<Vector3f>& triangle_list_positions_out,
  vector<Vector3f>& triangle_list_normals_out) {
  int cubeindex = 0;
  Vector3f position_list[12];
  Vector3f normal_list[12];

  // Determine the index into the edge table which tells us which vertices are
  // inside of the surface.
  if (grid_vals[0] < iso_level) {
    cubeindex |= 1;
  }
  if (grid_vals[1] < iso_level) {
    cubeindex |= 2;
  }
  if (grid_vals[2] < iso_level) {
    cubeindex |= 4;
  }
  if (grid_vals[3] < iso_level) {
    cubeindex |= 8;
  }
  if (grid_vals[4] < iso_level) {
    cubeindex |= 16;
  }
  if (grid_vals[5] < iso_level) {
    cubeindex |= 32;
  }
  if (grid_vals[6] < iso_level) {
    cubeindex |= 64;
  }
  if (grid_vals[7] < iso_level) {
    cubeindex |= 128;
  }

  // Cube is entirely in/out of the surface.
  if (kEdgeTable[cubeindex] == 0) {
    return;
  }

  // TODO(jiawen): just compute alpha separately

  // Find the vertices where the surface intersects the cube.
  if (kEdgeTable[cubeindex] & 1) {
    position_list[0] = ConsistentVertexInterp(iso_level,
      grid_positions[0], grid_positions[1], grid_vals[0], grid_vals[1]);
    normal_list[0] = VertexInterp(iso_level,
      grid_normals[0], grid_normals[1], grid_vals[0], grid_vals[1]).normalized();
  }
  if (kEdgeTable[cubeindex] & 2) {
    position_list[1] = ConsistentVertexInterp(iso_level,
        grid_positions[1], grid_positions[2], grid_vals[1], grid_vals[2]);
    normal_list[1] = VertexInterp(iso_level,
      grid_normals[1], grid_normals[2], grid_vals[1], grid_vals[2]);
  }
  if (kEdgeTable[cubeindex] & 4) {
    position_list[2] = ConsistentVertexInterp(iso_level,
      grid_positions[2], grid_positions[3], grid_vals[2], grid_vals[3]);
    normal_list[2] = VertexInterp(iso_level,
      grid_normals[2], grid_normals[3], grid_vals[2], grid_vals[3]);
  }
  if (kEdgeTable[cubeindex] & 8) {
    position_list[3] = ConsistentVertexInterp(iso_level,
      grid_positions[3], grid_positions[0], grid_vals[3], grid_vals[0]);
    normal_list[3] = VertexInterp(iso_level,
      grid_normals[3], grid_normals[0], grid_vals[3], grid_vals[0]);
  }
  if (kEdgeTable[cubeindex] & 16) {
    position_list[4] = ConsistentVertexInterp(iso_level,
      grid_positions[4], grid_positions[5], grid_vals[4], grid_vals[5]);
    normal_list[4] = VertexInterp(iso_level,
      grid_normals[4], grid_normals[5], grid_vals[4], grid_vals[5]);
  }
  if (kEdgeTable[cubeindex] & 32) {
    position_list[5] = ConsistentVertexInterp(iso_level,
      grid_positions[5], grid_positions[6], grid_vals[5], grid_vals[6]);
    normal_list[5] = VertexInterp(iso_level,
      grid_normals[5], grid_normals[6], grid_vals[5], grid_vals[6]);
  }
  if (kEdgeTable[cubeindex] & 64) {
    position_list[6] = ConsistentVertexInterp(iso_level,
      grid_positions[6], grid_positions[7], grid_vals[6], grid_vals[7]);
    normal_list[6] = VertexInterp(iso_level,
      grid_normals[6], grid_normals[7], grid_vals[6], grid_vals[7]);
  }
  if (kEdgeTable[cubeindex] & 128) {
    position_list[7] = ConsistentVertexInterp(iso_level,
      grid_positions[7], grid_positions[4], grid_vals[7], grid_vals[4]);
    normal_list[7] = VertexInterp(iso_level,
      grid_normals[7], grid_normals[4], grid_vals[7], grid_vals[4]);
  }
  if (kEdgeTable[cubeindex] & 256) {
    position_list[8] = ConsistentVertexInterp(iso_level,
      grid_positions[0], grid_positions[4], grid_vals[0], grid_vals[4]);
    normal_list[8] = VertexInterp(iso_level,
      grid_normals[0], grid_normals[4], grid_vals[0], grid_vals[4]);
  }
  if (kEdgeTable[cubeindex] & 512) {
    position_list[9] = ConsistentVertexInterp(iso_level,
      grid_positions[1], grid_positions[5], grid_vals[1], grid_vals[5]);
    normal_list[9] = VertexInterp(iso_level,
      grid_normals[1], grid_normals[5], grid_vals[1], grid_vals[5]);
  }
  if (kEdgeTable[cubeindex] & 1024) {
    position_list[10] = ConsistentVertexInterp(iso_level,
      grid_positions[2], grid_positions[6], grid_vals[2], grid_vals[6]);
    normal_list[10] = VertexInterp(iso_level,
      grid_normals[2], grid_normals[6], grid_vals[2], grid_vals[6]);
  }
  if (kEdgeTable[cubeindex] & 2048) {
    position_list[11] = ConsistentVertexInterp(iso_level,
      grid_positions[3], grid_positions[7], grid_vals[3], grid_vals[7]);
    normal_list[11] = VertexInterp(iso_level,
      grid_normals[3], grid_normals[7], grid_vals[3], grid_vals[7]);
  }

  // Create the triangle.
  for (int i = 0; kTriangleTable[cubeindex][i] != -1; i += 3) {
    triangle_list_positions_out.push_back(
      position_list[kTriangleTable[cubeindex][i]]);
    triangle_list_positions_out.push_back(
      position_list[kTriangleTable[cubeindex][i + 1]]);
    triangle_list_positions_out.push_back(
      position_list[kTriangleTable[cubeindex][i + 2]]);

    triangle_list_normals_out.push_back(
      normal_list[kTriangleTable[cubeindex][i]]);
    triangle_list_normals_out.push_back(
      normal_list[kTriangleTable[cubeindex][i + 1]]);
    triangle_list_normals_out.push_back(
      normal_list[kTriangleTable[cubeindex][i + 2]]);
  }
}

